// Bitwise stuff

// Not used in common programming language
// Used to store a lot data

// & - Binary AND - Copies a bit to the result if it exists in both operands
// | - Binary OR - Copies a bit to the result if it exists in either operand
// ^ - Binary XOR - Copies the bit if it is set in one operand but not both
// ~ - Binary Ones - Complement operator is unary and has the effect of flipping bits
// << - Binary Left Shift Operator - The left operands value is moved left by the number specified by the right operand
// >> - Binary Right Shift Operator - The left operands value is moved right by the number specified by the right operand